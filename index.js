"use strict";
/* eslint-disable node/global-require */
const XU = require("@sembiance/xu"),
	{minify} = require("html-minifier"),
	path = require("path");

const HTML_MINIFY_OPTIONS =
{
	caseSensitive                 : false,
	collapseBooleanAttributes     : true,
	collapseInlineTagWhitespace   : false,
	collapseWhitespace            : true,
	conservativeCollapse          : false,
	continueOnParseError          : false,
	decodeEntities                : false,
	html5                         : true,
	includeAutoGeneratedTags      : true,
	keepClosingSlash              : false,
	minifyCSS                     : true,
	preserveLineBreaks            : false,
	preventAttributesEscaping     : false,
	processConditionalComments    : false,
	quoteCharacter                : '"',
	removeAttributeQuotes         : false,
	removeComments                : true,
	removeEmptyAttributes         : true,
	removeEmptyElements           : false,
	removeOptionalTags            : false,
	removeRedundantAttributes     : false,
	removeScriptTypeAttributes    : false,
	removeStyleLinkTypeAttributes : false,
	removeTagWhitespace           : false,
	sortAttributes                : true,
	sortClassName                 : true
};

class HTML
{
	constructor(_baseDirPath)
	{
		this.baseDirPath = _baseDirPath;
	}

	render(subPath, data={}, options={})
	{
		const self=this;
		const htmlRaw = require(path.join(this.baseDirPath, subPath + ".js"))(data, {
			html : this.html.bind(this),
			include(includeSubPath, includeData=data) { return self.render(includeSubPath, includeData, {skipMinify : true}); } });

		return options.skipMinify ? htmlRaw : minify(htmlRaw, HTML_MINIFY_OPTIONS);
	}

	html(strs, ...vals)
	{
		const r = [];
		strs.forEach(str =>
		{
			r.push(str);

			if(vals.length>0)
				r.push(vals.shift());
		});

		return r.join("");
	}
}

exports.HTML = HTML;
